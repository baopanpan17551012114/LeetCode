"""
找出所有相加之和为 n 的 k 个数的组合。组合中只允许含有 1 - 9 的正整数，并且每种组合中不存在重复的数字。
说明：
所有数字都是正整数。
解集不能包含重复的组合
"""
import copy


def get_total_sets(n, k):
    path_list = []
    result_list = []
    back_track(result_list, path_list, k, n)
    print(result_list)


def back_track(result_list, path_list, k, n):
    # 满足条件
    if sum(path_list) == n and len(path_list) == k:
        # 解集不能重复？在哪里操作
        result_list.append(copy.deepcopy(path_list))
    for i in range(1, 10):
        # 数字不能重复
        if i in path_list:
            continue
        path_list.append(i)
        back_track(result_list, path_list, k, n)
        path_list.pop()


